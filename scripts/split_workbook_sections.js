function contents(sectionNo, title, topics, extraCount) {
  const topicItems = topics.map((t) => `    <li><span>${t}</span></li>`).join("\n");
  const extraItems = Array.from({ length: extraCount }, (_, i) => `    <li><span>Дополнительная тренировка ${i + 1}</span></li>`).join("\n");
  return `<section class="page sheet contents">
  <h2>Раздел ${sectionNo}. ${title}</h2>
  <div class="grp">Основные темы</div>
  <ul>
${topicItems}
    <li><span>Проверь себя — раздел ${sectionNo}</span></li>
  </ul>
  <div class="grp">Дополнительная отработка</div>
  <ul>
${extraItems}
    <li><span>Ответы</span></li>
  </ul>
  <div class="note remember"><span class="ico">📌</span><b>Маршрут слабого ученика</b>Иди по порядку: правило → готовый пример → задание с пропусками → 3 похожих примера. Если две ошибки подряд, не переходи к уровню «5»: вернись к правилу и реши первый пример заново.</div>
  <div class="note help"><span class="ico">🚑</span><b>Правило трёх попыток</b>1) Реши сам. 2) Проверь по ответу. 3) Если ошибка повторилась, выпиши коротко: «я перепутал ...», затем реши похожее задание.</div>
  <div class="page-foot"><span>Раздел ${sectionNo} · содержание</span><span></span></div>
</section>`;
}

function taskPage(sectionNo, title, subtitle, groups, n, errorBox = "") {
  const body = groups.map((g) => `<div class="step ${g.kind || ""}">
    <div class="step-label"><span class="num">${g.label || "✓"}</span>${g.title}</div>
    <div class="step-body">${g.html}</div>
  </div>`).join("\n");
  return `<section class="page">
  <div class="topic-head">
    <div class="topic-num">${sectionNo}.${n}</div>
    <div>
      <h3 class="topic-title">${title}</h3>
      <p class="topic-sub">${subtitle}</p>
    </div>
  </div>
  <div class="note help"><span class="ico">🚑</span><b>Перед решением</b>Подчеркни, что нужно найти. Запиши правило или формулу. После ответа сделай проверку обратным действием или прикидкой.</div>
${body}
${errorBox}
  <div class="self">
    <label>□ Я справился сам</label><label>□ Нужна тренировка</label><label>□ Разберу ошибку</label>
  </div>
  <div class="page-foot"><span>Раздел ${sectionNo} · дополнительная тренировка</span><span></span></div>
</section>`;
}

function answersPage(sectionNo, originalBlocks, extraAnswers) {
  const extras = extraAnswers.map((a) => `<div class="ans-block"><span class="t">${a.title}</span><br><span class="a">${a.answer}</span></div>`).join("\n");
  return `<section class="page answers">
  <h2>Ответы — Раздел ${sectionNo}</h2>
  <p class="lead">Сначала проверь ход решения, потом ответ. В задачах с единицами измерения ответ записывай с наименованием.</p>
  <div class="ans-grid">
${originalBlocks.join("\n")}
${extras}
  </div>
  <div class="page-foot"><span>Раздел ${sectionNo} · ответы</span><span></span></div>
</section>`;
}

function ol(items) {
  return `<ol>${items.map((x) => `<li>${x}</li>`).join("")}</ol>`;
}

function error(title, html) {
  return `<div class="note mistakes"><span class="ico">⚠️</span><b>${title}</b>${html}</div>`;
}

