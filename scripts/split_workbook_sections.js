const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const sourcePath = path.join(root, "tetrad.html");
const outDir = path.join(root, "sections");
const source = fs.readFileSync(sourcePath, "utf8");

fs.mkdirSync(outDir, { recursive: true });

const sectionRegex = /<section class="page[\s\S]*?<\/section>/g;
const pages = source.match(sectionRegex) || [];

function pageHas(page, text) {
  return page.includes(text);
}

function getRange(startText, endText) {
  const start = pages.findIndex((p) => p.includes('class="page sheet divider"') && pageHas(p, `<div class="lead">${startText}</div>`));
  const end = pages.findIndex((p, i) => i >= start && pageHas(p, endText));
  if (start < 0 || end < 0) throw new Error(`Range not found: ${startText} -> ${endText}`);
  return pages.slice(start, end + 1);
}

function extractAnswerBlocks(titles) {
  const blocks = [];
  const blockRe = /<div class="ans-block">[\s\S]*?<\/div>/g;
  const all = source.match(blockRe) || [];
  for (const title of titles) {
    const block = all.find((b) => b.includes(`<span class="t">${title}</span>`));
    if (block) blocks.push(block);
  }
  return blocks;
}

